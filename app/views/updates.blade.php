    <p>Hooray! Our request to vacate our portion of the paper street "Philadelphia Avenue" went to a public hearing last night in Susquehanna Township. The hearing lasted about five minutes, with no objections voiced. Then, about an hour later, the township commissioners (nine of them) voted unanimously to vacate. The two parcels will now abut, giving us about 7.7 contiguous acres. This was the first public airing of our project, an important milestone passed!</p>
    <p>Meanwhile, meetings with architect Rich Gribble continue, and plans for the new building's design move forward in leaps and bounds. </p>

