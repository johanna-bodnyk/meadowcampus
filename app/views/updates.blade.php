    <h3>Our first public airing, and a unanimous vote!</h3>
    <small>Posted September 12, 2014 by Jim Rietmulder</small>
    <div class="img-wrapper pull-left">
        <img src="images/updates/vacate.jpg">
        <p style="width:402px;">The blue outlines show The Circle School's future campus; the pink portion between them was a planned road dividing the two parcels. Susquehanna Township voted last night to abandon their plans for the road.</p>
